package com.example.BroadbandMobile;

public class BroadbandPoint
{
	private double _x;
	private double _y;

	public BroadbandPoint(){
		super();
	}
	
	public double getX() {
		return this._x;
	}

	public void setX(double x) {
		this._x = x;
	}

	public double getY() {
		return this._y;
	}

	public void setY(double y) {
		this._y = y;
	}
}

